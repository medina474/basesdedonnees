create or replace function ferie_within_saison_check()
 returns trigger as
$$
declare
    p daterange;
begin
    -- Récupère les dates de la saison liée
    select periode
    into p
    from saison
    where id = NEW.saison_id;

    -- Vérifie l'existence de la saison
    if p is null then
        raise exception 'La saison avec id % est introuvable.', NEW.saison_id;
    end if;

    -- Vérifie que le jour est dans la plage
    if not (NEW.jour <@ p) then
        raise exception 'Le jour férié (%) est en dehors de la saison (%)', NEW.jour, p;
    end if;

    return NEW;
end;
$$ language plpgsql;

create trigger ferie_within_saison_check_trigger
    before insert or update
    on ferie
    for each row
execute function ferie_within_saison_check();

