create view total_a_produire_semaine as
select
  extract(year from jour) as annee, extract(week from jour) as semaine,
  p.categorie, p.ordre, p.id as produit_id, p.produit, -- produit
  sum(qte)
from livraison l
  join produit p on p.id = l.produit_id
where livre = 'à livrer'
group by annee, semaine, p.id
--order by annee, semaine, categorie, ordre
;

