create or replace function planning_jour_valid()
  returns trigger as
$$
declare
  debut    date;
  fin      date;
  nb_ferie int;
begin
  -- Il doit exister une saison telle que le jour appartient à l’intervalle
  if not exists (
    select 1
    from calendrier c
    join saison s on c.saison_id = s.id
    where c.id = new.calendrier_id
      and new.jour between s.date_debut and s.date_fin
  ) then
    raise exception
      'Le % est invalide pour le calendrier %',
      new.jour, new.calendrier_id;
  end if;

  -- Vérifier que le jour n'est pas un jour férié
  if exists (
    select 1
    from ferie f
    join calendrier c on f.saison_id = c.saison_id
    where c.id = new.calendrier_id
      and f.jour = new.jour
  ) then
    raise exception
      'Le % est un jour férié pour cette saison.',
      new.jour;
  end if;

  -- Vérifier que le jour n'est pas dans une semaine de fermeture
  if exists (
    select 1
    from fermeture f
    join calendrier c on f.saison_id = c.saison_id
    where c.id = new.calendrier_id
      and f.semaine = extract(week from NEW.jour)
  ) then
    raise exception
      'Le % est dans une semaine de fermeture pour cette saison.',
      new.jour;
  end if;

  return NEW;
end;
$$ language plpgsql;

create trigger planning_jour_valid_trigger
  before insert or update
  on planning
  for each row
execute function planning_jour_valid();

