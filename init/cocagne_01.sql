create or replace procedure renumeroter_itineraires_tournee(p_tournee_id bigint)
language plpgsql
as $$
begin
  with numerotation as (
    select
      id,
      row_number() over (
        partition by tournee_id
        order by ordre, id
      ) as new_ordre
    from itineraire
    where tournee_id = p_tournee_id
  )
  update itineraire i
  set ordre = n.new_ordre
  from numerotation n
  where i.id = n.id;
end;
$$;

