create or replace procedure synchroniser_distribution_ordre()
language plpgsql
as $$
declare
    v_log_id bigint;
    v_created_itineraires integer := 0;
    v_updated_distribution integer := 0;
    v_anomalies integer := 0;
begin
    -- création entrée log
    insert into sync_distribution_log(message)
    values ('Début synchronisation distribution -> itineraire')
    returning id into v_log_id;


    /*
      1) Création itinéraires manquants (dépôt)
    */
    with manquants as (
        select distinct d.tournee_id, d.depot_id
        from distribution d
        where d.domicile = false
          and d.depot_id is not null
          and not exists (
              select 1
              from itineraire i
              where i.tournee_id = d.tournee_id
                and i.depot_id = d.depot_id
          )
    ),
    max_ordre as (
        select tournee_id, coalesce(max(ordre),0) as max_ordre
        from itineraire
        group by tournee_id
    ),
    a_inserer as (
        select
            m.tournee_id,
            m.depot_id,
            (coalesce(x.max_ordre,0)
             + row_number() over (partition by m.tournee_id order by m.depot_id))::smallint as new_ordre
        from manquants m
        left join max_ordre x on x.tournee_id = m.tournee_id
    )
    insert into itineraire(tournee_id, depot_id, adherent_id, ordre)
    select tournee_id, depot_id, null, new_ordre
    from a_inserer
    on conflict do nothing;

    get diagnostics v_created_itineraires = row_count;


    /*
      2) Création itinéraires manquants (domicile)
    */
    with manquants as (
        select distinct d.tournee_id, d.adherent_id
        from distribution d
        where d.domicile = true
          and d.adherent_id is not null
          and not exists (
              select 1
              from itineraire i
              where i.tournee_id = d.tournee_id
                and i.adherent_id = d.adherent_id
          )
    ),
    max_ordre as (
        select tournee_id, coalesce(max(ordre),0) as max_ordre
        from itineraire
        group by tournee_id
    ),
    a_inserer as (
        select
            m.tournee_id,
            m.adherent_id,
            (coalesce(x.max_ordre,0)
             + row_number() over (partition by m.tournee_id order by m.adherent_id))::smallint as new_ordre
        from manquants m
        left join max_ordre x on x.tournee_id = m.tournee_id
    )
    insert into itineraire(tournee_id, depot_id, adherent_id, ordre)
    select tournee_id, null, adherent_id, new_ordre
    from a_inserer
    on conflict do nothing;

    get diagnostics v_created_itineraires =  row_count;


    /*
      3) Mise à jour distribution.ordre
    */
    update distribution d
    set ordre2 = i.ordre
    from itineraire i
    where i.tournee_id = d.tournee_id
      and (
            (d.domicile = false and i.depot_id = d.depot_id)
         or (d.domicile = true  and i.adherent_id = d.adherent_id)
      );

    get diagnostics v_updated_distribution = row_count;


    /*
      4) Détection anomalies (cas impossibles)
    */
    select count(*)
    into v_anomalies
    from distribution d
    where (d.domicile = false and d.depot_id is null)
       or (d.domicile = true  and d.adherent_id is null);


    /*
      5) Fin log
    */
    update sync_distribution_log
    set finished_at = now(),
        created_itineraires = v_created_itineraires,
        updated_distribution = v_updated_distribution,
        anomalies = v_anomalies,
        message = case
            when v_anomalies > 0
                then 'Terminé avec anomalies (distribution incohérente)'
            else 'Terminé OK'
        end
    where id = v_log_id;

end;
$$;


