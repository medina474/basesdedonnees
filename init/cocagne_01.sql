create table fournisseur_prix
(
  id                 uuid default uuidv7() primary key,
  periode            tstzrange not null default tstzrange(current_timestamp, null, '[)') check (not isempty(periode)),
  article_id         bigint references article,
  fournisseur_id     bigint references fournisseur,
  ref_fournisseur    text,
  tarif              numeric(8,2), -- NULL = indisponible
  constraint fournisseur_prix_overlapping_exclusion
    exclude using gist (article_id with =, fournisseur_id with =, periode with &&)
);

create view fournisseur_prix_courant as
select * from fournisseur_prix
where upper_inf(periode);

-- Lors de l'insertion OLD est null

create or replace function fournisseur_prix_historique_insert()
  returns trigger
  language plpgsql
as $$
declare
  v_current fournisseur_prix%rowtype;
begin

  select * into v_current
  from fournisseur_prix
  where article_id     = NEW.article_id
    and fournisseur_id = NEW.fournisseur_id
    and upper_inf(periode);

  -- Si le tarif et la ref n'ont pas changé, on ne fait rien
  if found
    and v_current.tarif           is not distinct from NEW.tarif
    and v_current.ref_fournisseur is not distinct from NEW.ref_fournisseur
  then
    return null;
  end if;

  -- Clôture la ligne courante
  update fournisseur_prix
  set periode = tstzrange(lower(periode), current_timestamp)
  where id = v_current.id;

  -- Insère la nouvelle version
  insert into fournisseur_prix (periode, article_id, fournisseur_id, ref_fournisseur, tarif)
  values (tstzrange(current_timestamp, null), new.article_id, new.fournisseur_id, new.ref_fournisseur, new.tarif);

  return null;
end
$$;

create or replace function fournisseur_prix_historique_update()
  returns trigger
  language plpgsql
as $$
begin

  -- Si le tarif et la ref n'ont pas changé, on ne fait rien
  if    v_current.tarif           is not distinct from NEW.tarif
    and v_current.ref_fournisseur is not distinct from NEW.ref_fournisseur
  then
    return null;
  end if;

  -- Clôture la ligne courante
  update fournisseur_prix
  set periode = tstzrange(lower(periode), current_timestamp)
  where id = OLD.id;

  -- Insère la nouvelle version
  insert into fournisseur_prix (periode, article_id, fournisseur_id, ref_fournisseur, tarif)
  values (tstzrange(current_timestamp, null), new.article_id, new.fournisseur_id, new.ref_fournisseur, new.tarif);

  return null;
end
$$;

-- Le trigger est sur la vue

create trigger fournisseur_prix_trigger_update
instead of update on fournisseur_prix_courant
for each row
execute function fournisseur_prix_historique_update();

create trigger fournisseur_prix_trigger_insert
instead of insert on fournisseur_prix_courant
for each row
execute function fournisseur_prix_historique_insert();
