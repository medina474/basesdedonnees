create or replace function adherer(_adherent_id bigint, _saison_id bigint)
returns integer
language plpgsql
as $function$
declare
  _adhesion_id integer;
begin

  -- Vérifie si une adhésion existe déjà pour cette saison
  select id
  into _adhesion_id
  from adhesion
  where adherent_id = _adherent_id and saison_id = _saison_id;

  -- Si une adhésion existe déjà, la retourner
  if found then
    return _adhesion_id;
  end if;

  -- Sinon, insérer une nouvelle adhésion
  insert into adhesion (adherent_id, date_adhesion, montant, saison_id, mode_paiement_id)
  select a.id, now(), c.montant, _saison_id, a.mode_paiement_id
  from adherent a
  join cotisation c on c.profil_id = a.profil_id and c.saison_id = _saison_id
  where a.id = _adherent_id
  returning adhesion.id into _adhesion_id;

  -- Retourner la nouvelle adhésion
  return _adhesion_id;
end;
$function$;

